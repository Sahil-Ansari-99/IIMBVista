package com.iimbvista.iimbvista.Model;

public class AccommRule {
    public int index;
    public String text;

    public AccommRule(int index, String text) {
        this.index = index;
        this.text = text;
    }

    public int getIndex() {
        return index;
    }

    public String getText() {
        return text;
    }
}
